Line = input("Введите строку:")
print(f'Третий символ: {Line[2]}')
print("Предпоследний символ:", Line[-2])
print("Первые пять символов:", Line[0:5])
print("Вся, кроме последних двух:", Line[0:-2])
print("Все, с четными индексами:", Line[0::2])
print("Все, с нечетными индексами:", Line[1::2])
print("В обратном порядке:", Line[::-1])
print("В обратном порядке через один:", Line[::-2])
print("Длинна строки:", len(Line))